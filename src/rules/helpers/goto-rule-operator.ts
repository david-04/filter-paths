//----------------------------------------------------------------------------------------------------------------------
// Extract the "goto arrow" from the beginning of the line
//----------------------------------------------------------------------------------------------------------------------

export function getGotoRuleOperator(line: string) {
    return /^((\|\s*)<)(\s+|$)/i.exec(line)?.[1];
}

//----------------------------------------------------------------------------------------------------------------------
// Render
//----------------------------------------------------------------------------------------------------------------------

export function stringifyGotoRuleArrow(width: number) {
    return "|".padEnd(width - 1, " ");
}
